package execucao;

public class Principal {
	
	// Função principal de execução
	public static void main(String args[]){
		// Saida de dados
		System.out.println("Sejam bem vindos!");
		
		// Variaveis 
		// String -> texto -> " " 
		String nome = "Marcelo Luiz";
		
		// Character -> char -> ''
		Character letra = 'b';
		
		// Integer -> inteiros
		Integer numero = 10;
		
		// Double -> decimais
		Double decimal = 10.0;
		
		// Boolean -> lógico true/ false
		Boolean check = true;
		
		// Saida de dados
		System.out.println("Nome : " + nome);
		System.out.println("Inteiro : " + numero);
		System.out.println("Decimal : " + decimal);
		System.out.println("Logico : " +  check);
		System.out.println("Letra : " + letra);
					
		
		
	}

}
