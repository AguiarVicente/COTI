#include<stdio.h>
#include<stdlib.h>
/*
Estrutura repetição FOR

Para i de 1 a 10 faça
	bloco do para 
Fim para


Escreva um programa que imprima
os numeros inteiros de 0 a 10.

*/

int main(){
	
	// i++, i = i + 1, incremento da variavel i de 1 em 1
	for(int i = 0; i < 10; i++){
		printf("%d \n", i);
	}
	
	system("pause");
	return 0;
}




